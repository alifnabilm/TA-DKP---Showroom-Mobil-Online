    private void tenorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tenorActionPerformed
int harga = Integer.parseInt(txtharga.getText());
int cicil = 0;
if (tenor.getSelectedItem().equals("12")) {
  cicil = harga / 12;
} else if (tenor.getSelectedItem().equals("24")) {
  cicil = harga / 24;
} else if (tenor.getSelectedItem().equals("48")) {
  cicil = harga / 48;
}
cicilan.setText(String.valueOf(cicil));       
    }//GEN-LAST:event_tenorActionPerformed

