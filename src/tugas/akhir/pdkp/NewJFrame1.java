    private void pembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pembayaranActionPerformed
       if(pembayaran.getSelectedItem().equals("Tunai")){
           tenor.setEnabled(false);
           uangmuka.setEnabled(false);
           cicilan.setEnabled(false);
           tunai.setEnabled(true);
          
           
       }else if (pembayaran.getSelectedItem().equals("kredit")){
            tenor.setEnabled(true);
           uangmuka.setEnabled(true);
           cicilan.setEnabled(true);
           tunai.setEnabled(false);
       }
    }//GEN-LAST:event_pembayaranActionPerformed

